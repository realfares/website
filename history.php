    <h2>Significant Historical Events</h2>
    <p>
        Sur City, located on the coast of Oman, has long been known as a historic maritime hub. Dating back to the 6th century, it served as a vital port for trade with Africa, India, and Persia.
    </p>
    <p>
        Sur was renowned for building traditional wooden ships called <strong>dhows</strong>, which played a key role in trade and fishing.
    </p>
    <p>
        In the 19th century, Sur emerged as one of Oman’s most significant shipbuilding centers. The city’s strategic coastal position made it pivotal during the era of trade between Oman and Zanzibar.
    </p>
